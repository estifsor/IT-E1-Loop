using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CountNumbers
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Count Down from 10 to 1: Write a program that counts down from 10 to 1 using a loop.

            for (int i = 10; i >= 1; i--)
            {
                Console.WriteLine(i);
            }

        }
    }
}
